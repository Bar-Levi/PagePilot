// Helper function to get design token value
export function getToken(path: string): string {
    const keys = path.split('.');
    let value: any = designTokens;

    for (const key of keys) {
        value = value[key];
        if (value === undefined) {
            console.warn(`Design token not found: ${path}`);
            return '';
        }
    }

    return value;
}

